    private void buttonRuouActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonRuouActionPerformed
        // TODO add your handling code here:
                  // Lấy nội dung của nút buttonRuou
    String productName = buttonRuou.getText();

    // Lấy giá từ jLabelRuou và loại bỏ ký hiệu tiền tệ "VND", dấu phẩy và chuyển đổi sang số
    String priceString = jLabelRuou.getText().replace(".", "").replace(",", "").replace(" VND", "");
    double price = Double.parseDouble(priceString);

    boolean found = false;
    int rowCount = tableDV.getRowCount();
    for (int i = 0; i < rowCount; i++) {
        String existingProductName = (String) tableDV.getValueAt(i, 1);
        if (productName.equals(existingProductName)) {
            // Tăng số lượng và cập nhật giá mới cho hàng đã có trong bảng
            int quantity = (int) tableDV.getValueAt(i, 2);
            double totalPrice = Double.parseDouble(tableDV.getValueAt(i, 4).toString().replace(".", "").replace(",", "").replace(" VND", ""));
            quantity++;
            totalPrice += price;
            tableDV.setValueAt(quantity, i, 2);
            tableDV.setValueAt(String.format("%,.0f VND", totalPrice), i, 4);
            found = true;
            break;
        }
    }

    if (!found) {
        // Thêm hàng mới vào cuối bảng với số thứ tự được phát sinh
        Object[] rowData = new Object[] {counter++, productName, 1, null, String.format("%,.0f VND", price), null}; // Cột 1, 4 chưa có dữ liệu
        DefaultTableModel model = (DefaultTableModel) tableDV.getModel();
        model.addRow(rowData);
    }
    calculateTotalPrice();
    }//GEN-LAST:event_buttonRuouActionPerformed

