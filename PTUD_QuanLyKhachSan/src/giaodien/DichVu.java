// Phương thức tính toán tổng giá tiền và cập nhật lên textField giaDV
private void calculateTotalPrice() {
    double total = 0;

    // Lặp qua từng hàng trong bảng
    int rowCount = tableDV.getRowCount();
    for (int i = 0; i < rowCount; i++) {
        // Lấy giá trị của cột "Giá"
        String priceString = (String) tableDV.getValueAt(i, 4);
        // Kiểm tra xem dịch vụ có phải là "Miễn phí" không
        if (!"Miễn phí".equals(priceString)) {
            // Chuyển đổi giá thành số và cộng vào tổng
            double price = Double.parseDouble(priceString.replace(".", "").replace(",", "").replace(" VND", ""));
            total += price;
        }
    }

    // Hiển thị tổng giá trong textField giaDV
    giaDV.setText(String.format("%,.0f VND", total));
}


